package ch03;

public class ex05 {
    public static void main(String[] args) {
        int a = 10;
        int b = 4;
        System.out.printf("%d + %d = %d%n", a, b, a + b);
        System.out.printf("%d - %d = %d%n", a, b, a - b);
        System.out.printf("%d * %d = %d%n", a, b, a * b);
        System.out.printf("%d / %d = %d%n", a, b, a / b);
        System.out.printf("%d / %d = %3.1f%n", a, b, a / (float) b);
    }
}
